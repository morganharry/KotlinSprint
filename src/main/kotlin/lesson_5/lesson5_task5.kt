package lesson_5

const val NUM_OF_WIN_NUM = 2

fun main() {
    val listWin = mutableListOf<Int>()

    while (true) {
        val num = (1..100).random()
        if (!listWin.contains(num)) listWin.add(num)
        if (listWin.size == NUM_OF_WIN_NUM) break
    }

    print("–í–≤–µ–¥–∏—Ç–µ –¥–≤–∞ —á–∏—Å–ª–∞ –æ—Ç 1 –¥–æ 100 —á–µ—Ä–µ–∑ –ø—Ä–æ–±–µ–ª: ")
    val (userNum1, userNum2) = readln().split(" ").map { it.toInt() }

    when {
        listWin.contains(userNum1) && listWin.contains(userNum2) -> println("–ü–æ–∑–¥—Ä–∞–≤–ª—è–µ–º! –í—ã –≤—ã–∏–≥—Ä–∞–ª–∏ –≥–ª–∞–≤–Ω—ã–π –ø—Ä–∏–∑!")
        listWin.contains(userNum1) || listWin.contains(userNum2) -> println("–í—ã –≤—ã–∏–≥—Ä–∞–ª–∏ —É—Ç–µ—à–∏—Ç–µ–ª—å–Ω—ã–π –ø—Ä–∏–∑!")
        else -> println("–ù–µ—É–¥–∞—á–∞! –ö—Ä—É—Ç–∏—Ç–µ –±–∞—Ä–∞–±–∞–Ω!")
    }

    println("–í—ã–∏–≥—Ä—ã—à–Ω—ã–µ —á–∏—Å–ª–∞: ${listWin.joinToString()}")
}
